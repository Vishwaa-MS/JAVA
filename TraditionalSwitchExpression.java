public class TraditionalSwitchExpression {
    public static void main(String[] args) {
        char charValue = 'Z';
        switch (charValue) {
            case 'A':
                System.out.println("A is able");
                break;
            case 'B':
                System.out.println("B is Ball");
                break;
            case 'C':
                System.out.println("C is cat");
                break;
            default:
                System.out.println("OTHER ALPHABETS");
        }
    }
}
